'''გააკეთეთ 2-2 მაგალითი ლოგიკურ ოპერატორებზე: and, or, not. კომენტარების საშვლაებით გვერდით მიუწერეთ შედეგი. ახსენით რა არის ლოგიკური ოპერატორი და როგორ მუშაობს ლოგიკური ოპერაცია'''
print(True and False) # False
print(False and False) # False

print(True or False) # True
print(True or True) # True

print(not False) # True
print(not True) # False

# ლოგიკური ოპერატორებია and, or და not. ისინი bool ტიპის მონაცემებს კრიბავენ და გამოაქვს ერთი აუთფუთი. როცა and-ს ვიყენებთ, თუ bool მონაცემებიდან ერთი მაინცაა false, პასუხია false, or ის დროს საკმარისია ერთი true ც და პასუხი true არის. ხოლო not ი მონაცემს აბრუნებს. მაგალითად: not True-False  not False - True




