'''კომენტარებით ახსენით არამკაცრი და მკაცრი შედარების ოპერატორები.'''
# მკაცრი შედარების ოპერატორებია: >, <, == და !=, ხოლო არამკაცრებია: <= და >=. ასე იმიტო ქვია რომ სიტყვაზე რო დავწეროთ ორი ასეთი მაგალითი:
print(1 < 1)
print(1 <= 1)
# მეორე მაგალითში მიუხედავად იმისა რომ ერთი არ არის ერთზე ნაკლები, მაინც True ს გამოიტანდა რადგან ჩვენ გამოვიყენეთ არამკაცრი შედარების ოპერატორი (<=) ეს იმას ნიშნავს, რომ საკმარისია ტოლი მაინც იყოს მაინც true იქნებოდა. პირველ მაგალითში კი პასუხი False არის