'''2) for ციკლის საშვალებით 1-იდან 100-მდე გადაუარეთ ყველა რიცხვს, პირობითი განცხადების საშვალებით შეამოწმეთ რიცხვი ლუწია თუ კენტი და დაბეჭდეთ შესაბამისი შედეგი'''
for i in range (1,101):
    if i % 2 == 0:
        print(str(i) + 'is even number')
    elif i % 2 != 0:
        print(str(i) + 'is odd number')
    else:
        print('Error')